// ==================== เปลี่ยนมาใช้ jQuery slideDown แบบปรับปรุง ====================
document.addEventListener("click", function (e) {
  const target = e.target.closest(".column");
  if (!target) return;

  e.preventDefault();

  // ถ้าคลิกที่ column ที่เปิดอยู่แล้ว ให้ปิด
  if (target.classList.contains("on")) {
    closeDetail();
    return;
  }

  // ลบ class "on" จากทุก .column และปิด detail เก่า
  document.querySelectorAll(".column").forEach((col) => {
    col.classList.remove("on");
  });

  // ปิด detail เก่าก่อน (ถ้ามี)
  const oldDetail = document.querySelector(".column-detail.added");
  if (oldDetail) {
    jQuery(oldDetail).slideUp(350, function () {
      jQuery(this).remove();
    });
  }

  // นับจำนวน column ทั้งหมด
  const allItemsCount = document.querySelectorAll(
    ".l-collections__list.archive-list .column",
  ).length;

  // หาความกว้างหน้าจอ
  const width = window.innerWidth;

  // หา index ของ column ที่คลิก (เริ่มจาก 1)
  const allColumns = Array.from(document.querySelectorAll(".column"));
  const contNumber = allColumns.indexOf(target) + 1;
  const contLength = allColumns.length;

  // คำนวณจำนวน column ต่อแถว
  let columnsPerRow = 2;
  if (width > 1023) {
    columnsPerRow = 4;
  } else if (width > 767) {
    columnsPerRow = 3;
  }

  // คำนวณแถวที่ column อยู่
  const contLine = Math.ceil(contNumber / columnsPerRow);

  // คำนวณตำแหน่งสุดท้ายของแถว
  let lastNumber = columnsPerRow * contLine;

  // ถ้าเกินจำนวน column ทั้งหมด ให้ใช้ตัวสุดท้าย
  if (lastNumber > contLength) {
    lastNumber = allItemsCount;
  }

  // หา element ตำแหน่งสุดท้ายของแถว
  const lastElement = allColumns[lastNumber - 1]; // -1 เพราะ array เริ่มจาก 0

  // Scroll ไปยังตำแหน่ง
  if (lastElement) {
    const offsetTop = lastElement.offsetTop - 65;
    window.scrollTo({
      top: offsetTop,
      behavior: "smooth",
    });
  }

  // เพิ่ม class "on"
  target.classList.add("on");

  // สร้าง element ใหม่
  const item = findBrandItemById(target.id);
  if (!item) return;

  const cont = createColumnDetail(item);

  // ซ่อน element ก่อน (display: none)
  cont.style.display = "none";

  // แทรกหลัง lastElement
  if (lastElement) {
    lastElement.insertAdjacentElement("afterend", cont);
  }

  // ใช้ jQuery slideDown เพื่อเปิด
  jQuery(cont).slideDown(400, function () {
    // หลัง slideDown เสร็จ
    const slider = cont.querySelector(".owl-carousel");
    const show = cont.querySelector(".archive-drawarea");

    // ถ้ามี Owl Carousel
    if (slider && typeof jQuery !== "undefined" && jQuery.fn.owlCarousel) {
      jQuery(slider).owlCarousel({
        items: 1,
        singleItem: true,
        margin: 10,
        onInitialized: function () {
          if (show) {
            jQuery(show).animate({ opacity: 1 }, 1000);
          }
        },
      });
    } else if (show) {
      // ถ้าไม่ใช้ Owl Carousel ก็แค่ fade in
      jQuery(show).animate({ opacity: 1 }, 1000);
    }
  });
});

// ==================== ฟังก์ชันปิด Detail ====================
function closeDetail() {
  document.querySelectorAll(".column.on").forEach((col) => {
    col.classList.remove("on");
  });

  const detail = document.querySelector(".column-detail.added");
  if (!detail) return;

  jQuery(detail).slideUp(350, function () {
    jQuery(this).remove();
  });
}

// ==================== ปุ่มปิด - ใช้ jQuery slideUp ====================
document.addEventListener("click", function (e) {
  const closeBtn = e.target.closest(".close-archive");
  if (!closeBtn) return;

  e.preventDefault();
  e.stopPropagation();

  closeDetail();
});

