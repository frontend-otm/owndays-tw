function buildEyecampColumn(item) {
  return `
    <div id="${item.id}" class="column">
      <a href="#${item.id}" class="column-inner">
        <div class="filter ${item.description.length === 0 && "-no-filter"}">
          <img src="${item.thumbnail}" />
          <div>
            <span>${item.country}</span>${item.date}
            <p class="-city">${item.city}</p>
          </div>
          ${item.description.length > 0 ? '<span class="plus"></span>' : ""}
        </div>
        <span class="icon-search"></span>
      </a>
      <div class="column-content"></div>
    </div>`;
}

function renderEyecampItems() {
  const $list = $("#eyecamp-list");
  if (!$list.length) return;

  const html = eyecampItems.map((item) => buildEyecampColumn(item)).join("");
  $list.html(html);
}

function findEyecampItemById(id) {
  return eyecampItems.find((item) => item.id === id);
}

function createColumnDetail(item) {
  const $root = $("<div>", { class: "column-detail added" });

  /* archive-drawarea */
  const $drawArea = $("<div>", { class: "archive-drawarea" });
  // Initial opacity 0 effectively, will be animated to 1

  const $archiveBody = $("<div>", { class: "archive-body" });
  const $columnDescription = $("<div>", { class: "column-description" });
  const $csrDetail = $("<div>", { class: "l-csr__detail" });

  /* 1. Header (H3) */
  const $h3 = $("<h3>").html(
    `<span>${item.country}</span>${item.city} <span class="-date">${
      item.fullDate || item.date
    }</span>`,
  );
  $csrDetail.append($h3);

  /* 2. Slider */
  if (item.slides && item.slides.length > 0) {
    const $sliderWrap = $("<div>", { class: "l-csr__slider" });
    const $owl = $("<div>", { class: "owl-carousel owl-theme" });

    item.slides.forEach((src) => {
      $("<div>")
        .append($("<img>", { src: src, alt: "" }))
        .appendTo($owl);
    });

    $sliderWrap.append($owl);
    $csrDetail.append($sliderWrap);
  }

  /* 3. Text (Description) */
  if (item.description && item.description.length > 0) {
    const $textWrap = $("<div>", { class: "l-csr__text" });
    item.description.forEach((desc) => {
      $("<p>", { class: "-space" }).html(desc).appendTo($textWrap);
    });
    $csrDetail.append($textWrap);
  }

  /* 4. Testimonials */
  if (item.testimonials && item.testimonials.length > 0) {
    const $iconWrap = $("<div>", { class: "l-csr__icon" });
    const $ul = $("<ul>");

    item.testimonials.forEach((t) => {
      const $li = $("<li>");
      const $divHead = $("<div>");

      // Image (support both imageUrl from data and image from legacy code)
      const imgSrc = t.imageUrl || t.image;
      if (imgSrc) {
        $("<img>", { src: imgSrc, alt: t.name }).appendTo($divHead);
      }

      const $divInfo = $("<div>");
      $("<h4>").text(t.name).appendTo($divInfo);

      const $pRole = $("<p>");
      // Flag (support both flagUrl from data and flag from legacy code)
      const flagSrc = t.flagUrl || t.flag;
      if (flagSrc) {
        $("<img>", { src: flagSrc, alt: t.country || "" }).appendTo($pRole);
      }
      $pRole.append(document.createTextNode(t.role || ""));
      $divInfo.append($pRole);

      $divHead.append($divInfo);
      $li.append($divHead);

      // Comment (support both comment from data and text from legacy code)
      $("<p>")
        .html(t.comment || t.text || "")
        .appendTo($li);
      $ul.append($li);
    });

    $iconWrap.append($ul);
    $csrDetail.append($iconWrap);
  }

  /* 5. Video */
  if (item.hasVideo && item.videoUrl) {
    const $videoWrap = $("<div>", { class: "l-csr__video" });
    $("<iframe>", {
      width: "1120",
      height: "630",
      src: item.videoUrl,
      frameborder: "0",
      allow: "autoplay; encrypted-media",
      allowfullscreen: "allowfullscreen",
    }).appendTo($videoWrap);
    $csrDetail.append($videoWrap);
  }

  $columnDescription.append($csrDetail);
  $archiveBody.append($columnDescription);
  $drawArea.append($archiveBody);
  $root.append($drawArea);

  /* Close Button */
  const $closeA = $("<a>", { href: "#", class: "close-archive" });
  $closeA.html(
    '<span class="close-archive-inner"><svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20"><polygon points="16,15.3 10.7,10 16,4.7 15.3,4 10,9.3 4.7,4 4,4.7 9.3,10 4,15.3 4.7,16 10,10.7 15.3,16 "></polygon></svg></span>',
  );
  $root.append($closeA);

  return $root;
}

function closeDetail() {
  $(".column.on").removeClass("on");
  const $detail = $(".column-detail.added");
  if ($detail.length) {
    $detail.slideUp(350, function () {
      $(this).remove();
    });
  }
}

$(document).ready(function () {
  renderEyecampItems();

  // Click on Column to Open
  $(document).on("click", ".column", function (e) {
    const $column = $(this);
    const $clicked = $(e.target);
    // Prevent clicking if bubbling from close button (though .column usually wraps around)
    // Actually the close button is likely distinct from .column in DOM hierarchy if .column-detail is sibling.
    // But let's act on the .column itself.
    if ($(e.target).closest(".column-detail").length) return;

    const $target = $(this);
    e.preventDefault();

    // If active, close it
    if ($target.hasClass("on")) {
      closeDetail();
      return;
    }

    // Close any other open detail
    closeDetail();

    // Logic to find insertion point
    const $allColumns = $(".column");
    const width = $(window).width();
    const index = $allColumns.index($target);
    const count = $allColumns.length;

    let columnsPerRow = 2;
    if (width > 1023) columnsPerRow = 4;
    else if (width > 767) columnsPerRow = 3;

    // Determine row end
    const row = Math.ceil((index + 1) / columnsPerRow);
    let insertIndex = row * columnsPerRow - 1;
    if (insertIndex >= count) insertIndex = count - 1;

    const $lastElement = $allColumns.eq(insertIndex);

    if ($clicked.closest(".column").find(".-no-filter").length) {
      return;
    }

    // Update URL
    // const href = $target.find("a.column-inner").attr("href");
    // if (href) {
    //   history.pushState(null, null, href);
    // }

    // Scroll
    if ($lastElement.length) {
      const offsetTop = $lastElement.offset().top - 65;
      $("html, body").animate({ scrollTop: offsetTop }, 400);
    }

    $target.addClass("on");

    const item = findEyecampItemById($target.attr("id"));
    if (!item) return;

    const $cont = createColumnDetail(item);
    $cont.hide();

    if ($lastElement.length) {
      $lastElement.after($cont);
    } else {
      $("#eyecamp-list").append($cont);
    }

    $cont.slideDown(400, function () {
      const $slider = $cont.find(".owl-carousel");
      const $show = $cont.find(".archive-drawarea");

      if ($slider.length && $.fn.owlCarousel) {
        $slider.owlCarousel({
          items: 2,
          singleItem: true,
          margin: 5,
          dots: true,
          onInitialized: function () {
            $show.animate({ opacity: 1 }, 1000);
          },
          animateOut: "fadeOut",
          animateIn: "fadeIn",
        });
      } else {
        $show.animate({ opacity: 1 }, 1000);
      }
    });
  });

  // Click on Close Button
  $(document).on("click", ".close-archive", function (e) {
    e.preventDefault();
    closeDetail();
  });
});
